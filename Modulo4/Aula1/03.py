#Em projetos maiores, é comum que você precise gerenciar várias bibliotecas. Para documentar as dependências do projeto, você pode criar um arquivo requirements.txt que lista todos os módulos e suas versões necessárias.
#Exemplo de Arquivo
#requirements.txt
#requests==2.25.1
#numpy==1.19.5


#Para instalar todas as dependências listadas no arquivo requirements.txt, basta rodar o seguinte comando:
#pip install -r requirements.txt